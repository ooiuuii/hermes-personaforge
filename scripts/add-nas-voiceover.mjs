const voice = process.env.NAS_VOICE_NAME ?? "en-US-AndrewNeural";
const voiceRate = process.env.NAS_VOICE_RATE ?? "+0%";
const voiceVolume = Number(process.env.NAS_VOICE_VOLUME ?? "1.0");
const bgmVolume = Number(process.env.NAS_BGM_VOLUME ?? "0.085");
const outputGain = Number(process.env.NAS_OUTPUT_GAIN ?? "2.15");
const forceTts = process.env.FORCE_TTS === "1";

