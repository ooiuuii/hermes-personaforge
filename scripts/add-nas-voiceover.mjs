assertFile(bgm, "NAS BGM");

mkdirSync(dirname(textFile), { recursive: true });
mkdirSync(dirname(rawVoice), { recursive: true });
mkdirSync(dirname(fittedVoice), { recursive: true });
mkdirSync(dirname(mixedAudio), { recursive: true });
mkdirSync(dirname(output), { recursive: true });

if (!existsSync(textFile) || process.env.RESET_VOICE_TEXT === "1") {
  writeFileSync(textFile, `${defaultVoiceover}\n`, "utf8");
}

if (!existsSync(rawVoice) || forceTts) {
  runInherit(edgeTts, [
    "--voice",
    voice,
    `--rate=${voiceRate}`,
    "--file",
    textFile,
    "--write-media",
    rawVoice
  ]);
}

const videoDuration = readDuration(input);
const voiceDuration = readDuration(rawVoice);
const durationArg = formatSeconds(videoDuration);
const maxVoiceDuration = Math.max(1, videoDuration - 0.35);
const shouldFitShortVoice = process.env.FIT_SHORT_VOICE !== "0";
const voiceTempo =
  voiceDuration > maxVoiceDuration
    ? voiceDuration / maxVoiceDuration
    : shouldFitShortVoice && voiceDuration >= videoDuration * 0.9
      ? voiceDuration / maxVoiceDuration
      : 1;
const voiceFilter = [
  atempoChain(voiceTempo),
  `volume=${formatFilterNumber(voiceVolume)}`,
  "apad",
  `atrim=0:${durationArg}`,
  "asetpts=N/SR/TB",
  "aresample=48000"
].join(",");

runInherit(ffmpeg, [
  "-hide_banner",
  "-loglevel",
  "error",
  "-y",
  "-i",
  rawVoice,
  "-filter:a",
  voiceFilter,
  "-c:a",
  "aac",
  "-b:a",
  "160k",
  fittedVoice
]);

const fadeDuration = Math.min(5, Math.max(1.5, videoDuration * 0.04));
const fadeArg = formatSeconds(fadeDuration);
const fadeOutStart = formatSeconds(Math.max(0, videoDuration - fadeDuration));
const mixFilter = [
  "[0:a]volume=1.0[voice]",
  `[1:a]atrim=0:${durationArg},asetpts=PTS-STARTPTS,afade=t=in:st=0:d=${fadeArg},afade=t=out:st=${fadeOutStart}:d=${fadeArg},volume=${formatFilterNumber(bgmVolume)}[bgm]`,
  `[voice][bgm]amix=inputs=2:duration=first:dropout_transition=2,volume=${formatFilterNumber(outputGain)},alimiter=limit=0.95[aout]`
].join(";");

runInherit(ffmpeg, [
  "-hide_banner",
  "-loglevel",
  "error",
  "-y",
  "-i",
  fittedVoice,
  "-i",
  bgm,
  "-filter_complex",
  mixFilter,
  "-map",
  "[aout]",
  "-c:a",
  "aac",
  "-ar",
  "48000",
  "-b:a",
  "192k",
  mixedAudio
]);

runInherit(ffmpeg, [
  "-hide_banner",
  "-loglevel",
  "error",
  "-y",
  "-i",
  input,
  "-i",
  mixedAudio,
  "-map",
  "0:v:0",
  "-map",
  "1:a:0",
  "-c:v",
  "copy",
  "-c:a",
  "aac",
  "-ar",
  "48000",
  "-b:a",
  "192k",
  "-shortest",
  output
]);

const mixedDuration = readDuration(mixedAudio);
const outputDuration = readDuration(output);

