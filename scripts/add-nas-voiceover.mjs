function assertFile(path, label) {
  if (!existsSync(path)) {
    const hint =
      label === "NAS BGM"
        ? " Set NAS_BGM_SOURCE to a local instrumental music file before running npm run mix:nas-voice."
        : "";
    throw new Error(`${label} not found: ${path}.${hint}`);
  }
}

function run(command, args) {
  const result = spawnSync(command, args, {
    cwd: root,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"]
  });

  if (result.status !== 0) {
    const stderr = result.stderr?.trim();
    throw new Error(`${command} failed${stderr ? `: ${stderr}` : ""}`);
  }

  return result.stdout.trim();
}

function runInherit(command, args) {
  const result = spawnSync(command, args, {
    cwd: root,
    stdio: "inherit"
  });

  if (result.status !== 0) {
    throw new Error(`${command} failed with exit code ${result.status}`);
  }
}

function readDuration(path) {
  const durationText = run(ffprobe, [
    "-v",
    "error",
    "-show_entries",
    "format=duration",
    "-of",
    "default=noprint_wrappers=1:nokey=1",
    path
  ]);
  const duration = Number(durationText);

  if (!Number.isFinite(duration) || duration <= 0) {
    throw new Error(`Could not read a valid duration from ${path}`);
  }

  return duration;
}

function formatSeconds(value) {
  return value.toFixed(3).replace(/\.?0+$/, "");
}

function formatFilterNumber(value) {
  return value.toFixed(6).replace(/\.?0+$/, "");
}

function atempoChain(tempo) {
  const filters = [];
  let remaining = tempo;

  while (remaining > 2) {
    filters.push("atempo=2");
    remaining /= 2;
  }

  while (remaining < 0.5) {
    filters.push("atempo=0.5");
    remaining /= 0.5;
  }

  filters.push(`atempo=${formatFilterNumber(remaining)}`);
  return filters.join(",");
}

if (!Number.isFinite(voiceVolume) || voiceVolume <= 0 || voiceVolume > 2) {
  throw new Error("NAS_VOICE_VOLUME must be a number between 0 and 2.");
}

if (!Number.isFinite(bgmVolume) || bgmVolume < 0 || bgmVolume > 1) {
  throw new Error("NAS_BGM_VOLUME must be a number between 0 and 1.");
}

if (!Number.isFinite(outputGain) || outputGain <= 0 || outputGain > 4) {
  throw new Error("NAS_OUTPUT_GAIN must be a number between 0 and 4.");
}

